function plotClusters(x,y,idx,xl,yl,ti,colorArr)
    [figNew,pos] = CreateFig();
    set(0,'Units','pixels')
    set(figure(figNew),'OuterPosition',...
            [pos(1) pos(2) 400 400])
%     colorArr = [0.2 0.2 0.9;...
%                 0.9 0.2 0.2;...
%                 0.2 0.9 0.2;...
%                 0.6 0.6 0.6;...
%                 0.3 0.3 0.7;...
%                 0.3 0.7 0.3;...
%                 0.2 0.5 0.8;...
%                 0.2 0.8 0.5;...
%                 0.8 0.5 0.2];
            
%     colorArr = [0.5 0.5 0.9;...
%         163/255 207/255 98/255;...
%         234/255 131/255 114/255;...
%         0.7 0.3 0.3;...
%         0.5 0.9 0.5;...
%         0.2 0.5 0.8;...
%         0.2 0.8 0.5;...
%         0.8 0.5 0.2;...
%         0.3 0.7 0.3];
    hold on;
    for i = max(idx):-1:1
        indTmp = idx == i;
        h = plot(x(indTmp),y(indTmp),'.');
        set(h,'MarkerSize',5,'Color',colorArr(i,:));
    end
%     h = plot(xt,yt,'k+');
%     set(h,'MarkerSize',7);
    maxX = max(x);
    maxY = max(y);
    minX = min(x);
    minY = min(y);
    set(gca,'XLim',[minX maxX],'YLim',[minY maxY]);
    xlabel(xl)
    ylabel(yl)
    title(ti);
end